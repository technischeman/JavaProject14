public class attributes {
    private String name;
    private int age;
    private String country;

    //getter
    public String getName() {
        return name;
    }

    //setter
    public void setName(String x) {
        this.name = x;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int a) {
        this.age = a;

    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String c) {
        this.country = c;
    }
}



